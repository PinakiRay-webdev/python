# Tuples in python
# It is a built-in data structure in python that allow us to create an immutable sequence of values
tup = (45, 50, 34, 48, 39, 45)
print(tup)

# methods in tuples
print(tup.index(39))    # returns the index number of the element
print(tup.count(45))    # returns the number of occurrence of that element

